    private void Add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_btnActionPerformed
        
        //JOptionPane.showMessageDialog(null, "Sem : " +Sem);
        
        String OfferdYear =offerdYr_cb.getSelectedItem().toString();
        String SubName = SubName_tf.getText();
        String SubCode = subCode_tf.getText();
        String LecHours = Lec_hours.getValue().toString();
        String TuteHours = tuteHours.getValue().toString();
        String LabBours = LabHours.getValue().toString();       
        String EvloluationHours = EvoHours.getValue().toString();
                
        
       if(OfferdYear == "Select")
       {
                JOptionPane.showMessageDialog(null, "Please Select Offerd Year");
                
         }
       else
       {
           if(validateFields() && validateAdd()){
            try
            {
                DataBase.setData("insert into subjects(year,semester,subname,subcode,lecHours,tuteHours,labHours,EvalutionHours) values('"+OfferdYear+"','"+Sem+"','"+SubName+"','"+SubCode+"','"+LecHours+"','"+TuteHours+"','"+LabBours+"','"+EvloluationHours+"')");
                JOptionPane.showMessageDialog(null, "Subject Successfully Added");
                
                 Refresh();
                 Sem = "";

            }
            catch (Exception e)
            {
                        JOptionPane.showMessageDialog(null, "Failed" + e);
            }
           }
            
            LoadTable();
       }
    }//GEN-LAST:event_Add_btnActionPerformed

    private void Update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_btnActionPerformed
       
        String OfferdYear =offerdYr_cb.getSelectedItem().toString();
        String SubName = SubName_tf.getText();
        String SubCode = subCode_tf.getText();
        String LecHours = Lec_hours.getValue().toString();
        String TuteHours = tuteHours.getValue().toString();
        String LabBours = LabHours.getValue().toString();       
        String EvloluationHours = EvoHours.getValue().toString();
        
        if(Sem1.isSelected()){
        
            Sem = "1";
        }else if(Sem2.isSelected()){
        
            Sem = "2";
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        int row = jTable2.getSelectedRow();

        if (row != -1) 
        {
            if(OfferdYear == "Select")
            {
                JOptionPane.showMessageDialog(null, "Select Offerd Year");
            }
            else
            {
                int confirm = JOptionPane.showConfirmDialog(null, "Do you want to Update?", "Update", JOptionPane.YES_NO_OPTION); 
                if (confirm == 0) 
                {
                    try
                    {
                        
                        if(validateFields()){
                        
                        DataBase.setData("UPDATE subjects set year = '"+OfferdYear+"', Semester = '"+Sem+"', subname = '"+SubName+"',subcode = '"+SubCode+"',lecHours = '"+LecHours+"',tuteHours = '"+TuteHours+"',labHours = '"+LabBours+"',EvalutionHours = '"+EvloluationHours+"' where ID = '"+PrimaryKey+"' ");
                        JOptionPane.showMessageDialog(null, " Successfully Updated");
                      
                         Refresh();
                        }
                    }
                    catch (Exception e)
                    {
                              JOptionPane.showMessageDialog(null, "Failed" + e);
                    }
               
                  LoadTable();
                }
            }
        }
        
        else 
        {
                JOptionPane.showMessageDialog(null, "Please Select a row for Update");
        }
     
    }//GEN-LAST:event_Update_btnActionPerformed

