    public boolean validateAdd(){
        
        String code = subCode_tf.getText();
        String exist = "";
    
        try
          {
  
                ResultSet ex = DataBase.getData("SELECT `subcode` FROM `subjects` WHERE `subcode` = '" +code+ "' GROUP BY `subcode` HAVING COUNT(*) > '-1'");

                while (ex.next())
                {
                    exist = ex.getString("subcode");
                    
                }
            } 
        catch (Exception e) 
        {
                
            JOptionPane.showMessageDialog(null, "Failed" + e);
        }
        
        if (exist != ""){
            
            JOptionPane.showMessageDialog(null, "Subject already exists under given Subject Code");
            return false;
        }
    
        return true;
    }
     
     
