    public void LoadTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        try {
              
                ResultSet rs = DataBase.getData("Select EmpID, Lecturer_name, Monday, Tuesday, Wednesday, Thursday, Friday, Saturday, Sunday from activehours");
                
                while (rs.next())
                {

                    Vector v = new Vector();
                   
                    v.add(rs.getString(1));
                    v.add(rs.getString(2));  
                    v.add(rs.getString(3));  
                    v.add(rs.getString(4));  
                    v.add(rs.getString(5));
                    v.add(rs.getString(6));  
                    v.add(rs.getString(7));  
                    v.add(rs.getString(8));
                    v.add(rs.getString(9));
              
                     
                   

                    dtm.addRow(v);

                }

            } catch (Exception e) 
            {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
    }
    
     public String SaveTime(String Start, String End)
    {
        String Time = "";
        if("Select".equalsIgnoreCase(Start) || "Select".equalsIgnoreCase(End))
        {
            Time = "--";
           //return Start;
        }
        
        else
        {
            Time = Start + "-" + End;
            
        }
        return Time;
    }
     
