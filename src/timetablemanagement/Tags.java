    public void viewDetails()
    {
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
        
        try {
              
                ResultSet rs = DataBase.getData("Select id, tagname, tagcode,relatedtag from tag");
                
                while (rs.next())
                {

                    Vector v = new Vector();
                   
                    v.add(rs.getString(1));
                    v.add(rs.getString(2));  
                    v.add(rs.getString(3));  
                    v.add(rs.getString(4));  
                     
                   

                    dtm.addRow(v);

                }

            } catch (Exception e) 
            {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
    }
    
      private void fillComboTagName(){
  
        try{
          
            ResultSet rs = DataBase.getData("SELECT subname AS sub FROM subjects order by id");
             
            while(rs.next()){
            
                String tagname = rs.getString("sub");
                combotagname.addItem(tagname);
            }
        }catch(Exception e){
        
            JOptionPane.showMessageDialog(rootPane, e);
        }        
    } 

